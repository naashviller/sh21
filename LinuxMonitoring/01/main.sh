#!/bin/bash

if [ -n "$2" ]
then
    echo "Указано больше параметров, чем необходимо, укажите 1 параметр."
else
    if [ -n "$1" ]
    then
        if [[ $1 =~ ^[+,-]?[0-9]+([.][0-9]+)?$ ]]
        then
            echo "Параметр неправильного формата, введите строку."
        else
            echo "$1"
        fi
    else
        echo "Указаны не все параметры , укажите 1 параметр."
    fi
fi
